#!/bin/sh
# Generate THIRD-PARTY license attribution files for npm packages (sh+jq port
# of the former generate-js-attribution.py).
#
# Reads package-lock.json files for the runtime npm footprints (app, sdk/web)
# and emits both a machine-readable JSON summary and a human-readable text
# attribution file into the distribution's licenses/ directory. Wired into the
# Trunk pre_build hook so the attribution cannot go stale.
#
# Usage: generate-js-attribution.sh [output-dir]   (default: dist/licenses)
set -eu

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
OUTPUT_DIR="${1:-$REPO_ROOT/dist/licenses}"

if ! command -v jq >/dev/null 2>&1; then
    echo "Error: jq is required but was not found in PATH" >&2
    exit 2
fi

mkdir -p "$OUTPUT_DIR"
NOW="$(date -u +%Y-%m-%dT%H:%M:%S+00:00)"

generate() {
    label="$1"
    lockfile_rel="$2"
    lockfile_path="$REPO_ROOT/$lockfile_rel"

    if [ ! -f "$lockfile_path" ]; then
        echo "Warning: $lockfile_path not found, skipping." >&2
        return 0
    fi

    deps="$(jq -c '
        def normlic:
            if type == "object" then (.type // "UNKNOWN")
            elif type == "array" then (map(tostring) | join(" OR "))
            else . end;
        [.packages | to_entries[] | select(.key != "") | {
            name: (.key | sub("node_modules/"; ""; "g")),
            version: (.value.version // ""),
            license: ((.value.license // "UNKNOWN") | normlic),
            path: .key
        }]' "$lockfile_path")"
    count="$(printf '%s' "$deps" | jq 'length')"

    json_path="$OUTPUT_DIR/THIRD-PARTY-$label.json"
    txt_path="$OUTPUT_DIR/THIRD-PARTY-$label.txt"

    jq -n --argjson deps "$deps" --arg generated_at "$NOW" \
          --arg src "$lockfile_rel" --arg label "$label" '{
        generated_by: "scripts/generate-js-attribution.sh",
        generated_at: $generated_at,
        source_lockfile: $src,
        package_set: $label,
        dependency_count: ($deps | length),
        dependencies: $deps
    }' > "$json_path"

    {
        printf 'THIRD-PARTY LICENSES — %s\n' "$label"
        printf 'Generated by scripts/generate-js-attribution.sh\n'
        printf 'Generated at: %s\n' "$NOW"
        printf 'Source: %s\n' "$lockfile_rel"
        printf 'Total dependencies: %s\n\n' "$count"
        printf 'This file lists npm dependencies and their identified licenses.\n'
        printf 'It is a build-time generated artifact; do not edit by hand.\n\n'
        printf '%s' "$deps" | jq -r '
            group_by(.license) | sort_by(.[0].license)[]
            | "== \(.[0].license) ==",
              (sort_by(.name)[]
               | "  \(.name)@\(.version | if . == "" then "unknown" else . end)"),
              ""'
    } > "$txt_path"

    echo "Generated $json_path ($count dependencies)"
    echo "Generated $txt_path ($count dependencies)"
}

# circuits/src/circomlib is intentionally excluded here: its npm dependencies
# are build/test-time-only tooling (see the GPL-3.0/LGPL-3.0 exceptions in
# .github/js-license-policy.json), never shipped in a distributed artifact —
# do not add a third `generate` call for it without re-checking that.
generate "app" "app/package-lock.json"
generate "sdk-web" "sdk/web/package-lock.json"
